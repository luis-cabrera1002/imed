// Funciones auxiliares para BAM
export const getBamHospitalById = (id: string): Clinic | undefined => {
  return bamHospitals.find(hospital => hospital.id === id);
};

export const getBamDoctorById = (id: string): Doctor | undefined => {
  return bamDoctors.find(doctor => doctor.id === id);
};

export const getBamDoctorsBySpecialty = (specialty: string): Doctor[] => {
  return bamDoctors.filter(doctor => doctor.specialty === specialty);
};

export const getBamDoctorsByCity = (city: string): Doctor[] => {
  const hospitalIds = bamHospitals
    .filter(h => h.city.toLowerCase().includes(city.toLowerCase()))
    .map(h => h.id);
  
  return bamDoctors.filter(doctor => 
    doctor.clinicIds.some(id => hospitalIds.includes(id))
  );
};

export const getBamHospitalsByCity = (city: string): Clinic[] => {
  return bamHospitals.filter(h => 
    h.city.toLowerCase().includes(city.toLowerCase())
  );
};
